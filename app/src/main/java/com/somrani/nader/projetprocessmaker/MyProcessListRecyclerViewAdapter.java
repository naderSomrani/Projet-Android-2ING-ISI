import android.widget.TextView;

import com.somrani.nader.projetprocessmaker.ProcessListFragment.OnListFragmentInteractionListener;
import com.somrani.nader.projetprocessmaker.dummy.DummyContent.DummyItem;
import com.somrani.nader.projetprocessmaker.interfaces.ProcessItem;

import java.util.ArrayList;
import java.util.List;

/**
 * {@link RecyclerView.Adapter} that can display a {@link DummyItem} and makes a call to the
 * specified {@link OnListFragmentInteractionListener}.
 * TODO: Replace the implementation with code for your data type.
 */
public class MyProcessListRecyclerViewAdapter extends RecyclerView.Adapter<MyProcessListRecyclerViewAdapter.ViewHolder> {

    private List<ProcessItem> mValues;
    private final OnListFragmentInteractionListener mListener;

    public MyProcessListRecyclerViewAdapter(List<ProcessItem> items, OnListFragmentInteractionListener listener) {
        mValues = new ArrayList<ProcessItem>();
        mListener = listener;
    }

    @Override
    public ViewHolder onCreateViewHolder(ViewGroup parent, int viewType) {
        View view = LayoutInflater.from(parent.getContext())
                .inflate(R.layout.fragment_processlist, parent, false);
        return new ViewHolder(view);
    }

    @Override
    public void onBindViewHolder(final ViewHolder holder, int position) {
        holder.mItem = mValues.get(position);
        holder.mContentView.setText(mValues.get(position).pro_title);

        holder.mView.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                if (null != mListener) {
                    // Notify the active callbacks interface (the activity, if the
                    // fragment is attached to one) that an item has been selected.
                    mListener.onListFragmentInteraction(holder.mItem);
                }
            }
        });
    }

    public void updateItems(List<ProcessItem> items){
        this.mValues = items;
        System.out.println(mValues);
    }

    @Override
    public int getItemCount() {
        return mValues.size();
    }

    public class ViewHolder extends RecyclerView.ViewHolder {
        public final View mView;
        public final TextView mIdView;
        public final TextView mContentView;
        public ProcessItem mItem;

        public ViewHolder(View view) {
            super(view);
            mView = view;
            mIdView = (TextView) view.findViewById(R.id.item_number);
            mContentView = (TextView) view.findViewById(R.id.content);
        }

        @Override
        public String toString() {
            return super.toString() + " '" + mContentView.getText() + "'";
        }
    }
}
